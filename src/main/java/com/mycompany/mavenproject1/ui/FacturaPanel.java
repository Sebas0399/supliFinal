    private static int contarTotalValores(Map<List<List<String>>, List<List<String>>> items) {
        int total = 0;

        // Iterar a trav√©s de los valores del mapa y contar
        for (List<List<String>> value : items.values()) {
            total += value.size();
        }

        return total;
    }

